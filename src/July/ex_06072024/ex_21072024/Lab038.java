public class Lab038 {
    public static void main(String[] args) {
        //loops
        // For,while and do while loops
        //to perform action multiples
        //print number 1 to 10
        for(int i=1; i<=10;i++)
        {
            System.out.println(i);
        }
        int j=1;
        while( j<=10)
        {
            System.out.println(j);
            j++;
        }
 int k =1;
        do {
            System.out.println("hi" + k);
            ++k;
        }while(k<=10);
    }
}
