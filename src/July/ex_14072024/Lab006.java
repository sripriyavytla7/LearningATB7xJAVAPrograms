package July.ex_14072024;

public class Lab006 {
    public static void main(String[] args) {
       int a =10;

       //increment operators
        //pre_increment - First we will increase the value of expression(++a) and later we will assign increase the value to a
        int A = ++a;
        System.out.println(A);
        System.out.println(a);

        //post increment  operator  - First we will assign value to  expression(b++) and later we will increase the value of b
        int b=10;
        int B = b++;
        System.out.println(B);
        System.out.println(b);

    }


}
