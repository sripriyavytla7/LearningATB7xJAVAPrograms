package ex_21072024;

import java.util.Scanner;

public class Lab055 {
    public static void main(String[] args) {
        //loops
        // For,while and do while loops
   //Different between while and Do while
        int i = 10;
        while(i<10)
        {
            System.out.println(i);
            i++;
        }

        do {
            System.out.println("Do while");
            System.out.println(i);
            i++;
        }while(i<10);

    }
};
